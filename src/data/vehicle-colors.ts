export function getColorsForBrand(brand: string): VehicleColor[] {
  const specific = BRAND_COLORS[brand] ?? [];
  const specificSet = new Set(specific);
  const generic = GENERIC_COLORS.filter((c) => !specificSet.has(c));
  return [...specific, ...generic].map((name) => ({ id: name, name }));
}
